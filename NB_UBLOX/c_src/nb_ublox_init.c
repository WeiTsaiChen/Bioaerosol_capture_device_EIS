void nb_ublox_init()
{
	switch (NB_UBLOX_BAUDRATE)
    {
        case 1200:
            gb_nb_ublox_timer_value=32;
            break;
        case 2400:
            gb_nb_ublox_timer_value=16;
            break;
        case 4800:
            gb_nb_ublox_timer_value=8;
            break;
        case 9600:
            gb_nb_ublox_timer_value=4;
            break;
		case 115200:	
        case 38400:
        case 57600:
        case 19200:
            gb_nb_ublox_timer_value=32;
            break;
    }
	nb_ublox_sfr_init();
	nb_ublox_glb_vars_init();
}
